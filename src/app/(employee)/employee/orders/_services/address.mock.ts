// Helper function để lấy mock states
export const getMockStatesByCountry = (country: string) => {
  if (country === "Việt Nam") {
    return MOCK_STATES_VN;
  }
  if (country === "Hoa Kỳ") {
    return MOCK_STATES_US;
  }
  // Default: trả về một số states mẫu
  return [
    { state_code: "ST1", name: `${country} - State 1` },
    { state_code: "ST2", name: `${country} - State 2` },
    { state_code: "ST3", name: `${country} - State 3` },
  ];
};

// Helper function để lấy mock cities
export const getMockCitiesByState = (country: string, state: string) => {
  if (country === "Việt Nam") {
    if (state === "Hà Nội") {
      return MOCK_CITIES_HN;
    }
    if (state === "Hồ Chí Minh") {
      return MOCK_CITIES_HCM;
    }
    if (state === "Đà Nẵng") {
      return MOCK_CITIES_DN;
    }
    // Default cho các tỉnh khác của VN
    return [
      `${state} - Phường 1`,
      `${state} - Phường 2`,
      `${state} - Phường 3`,
      `${state} - Xã 1`,
      `${state} - Xã 2`,
    ];
  }
  if (country === "Hoa Kỳ" && state === "California") {
    return MOCK_CITIES_CA;
  }
  // Default cho các quốc gia/state khác
  return [
    `${state} - City 1`,
    `${state} - City 2`,
    `${state} - City 3`,
  ];
};

